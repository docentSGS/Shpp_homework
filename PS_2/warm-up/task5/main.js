const userLinks = document.getElementById('user_input');
userLinks.onblur = prepareLinkText;

function prepareLinkText() {
  let inputData = userLinks.value;
  let splitedData = inputData.split(',');

  intermediateDataArray = splitedData.map(function(e) {
    return e.replace(/\s/g, '');
  });
  intermediateDataArray = intermediateDataArray.map(function(e) {
    return e.replace(/^https?:\/\//, '');
    });
    let finishedData = intermediateDataArray.map(validUrlAndIp).filter(Boolean);
    finishedData.sort();
    makeLink(finishedData);
  }

  function makeLink(finishedData) {
    const result = document.getElementById('result');
    result.innerHTML = '';
    if (finishedData.length===0) {
      result.innerHTML = 'Please, enter data';
      return;
    }

    for (let i = 0; i < finishedData.length; i++) {
      let link = document.createElement('a');
      let linkTitle = document.createTextNode(finishedData[i]);
      link.appendChild(linkTitle);
      link.target = 'blank';
      link.href = finishedData[i];
      link.innerHTML = finishedData[i] + '<br>';
      result.appendChild(link);
    }
  }

